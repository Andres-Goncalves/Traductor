#recibe el idioma de origen y el idioma destino de la tradución, y los usa para obtener los codigos de idioma correspondientes
def recuperar_codigos_de_idioma(idioma_app, indice_idioma_origen, indice_idioma_destino):
    for i in TRADUCTOR_DIC[idioma_app]:
        if indice_idioma_origen == i[0]:
            indice_idioma = i[1]
            break
    
    idioma_OCR = ''
    idioma_o = ''
    for i in CODIGOS:
        if i[0] == indice_idioma:
            idioma_o = i[1]
            idioma_OCR = i[2]
            break

    for i in TRADUCTOR_DIC[idioma_app]:
        if indice_idioma_destino == i[0]:
            indice_idioma = i[1]
            break

    idioma_d = ''
    for i in CODIGOS:
        if i[0] == indice_idioma:
            idioma_d = i[1]
            break

    return idioma_OCR, idioma_o, idioma_d